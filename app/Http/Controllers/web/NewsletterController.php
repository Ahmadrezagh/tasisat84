     */
    public function store(Request $request)
    {
        //
        $validatedData = $request->validate([
            'mail' => 'required|email|max:255',
        ]);
        if(count(Newsletter::where('mail', '=', $request->mail)->get())>0){
            alert()->warning('شما قبلا در خبرنامه عضو شده اید');
            return back();
        }else{
            Newsletter::create([
                'mail' => $request->mail,
            ]);
            alert()->success('شما با موفقیت در خبرنامه عضو شدید');
            return back();
        }
    }

